#include <iostream>
using namespace std;

int main()
{
    // all arithmetic operation

    int iNo1 = 0;
    int iNo2 = 0;

    // Input

    cout << "Enter the First value" << endl;
    cin >> iNo1;

    cout << "Enter the Second value" << endl;
    cin >> iNo2;

    // Output

    cout << "Sum is " << iNo1 + iNo2 << endl;
    cout << "Sub is " << iNo1 - iNo2 << endl;
    cout << "Mult is " << iNo1 * iNo2 << endl;
    cout << "Div is " << iNo1 / iNo2 << endl;

    return 0;
}